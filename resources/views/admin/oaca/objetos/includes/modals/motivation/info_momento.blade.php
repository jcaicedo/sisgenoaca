                  <h3 style="color:black;">Desarrollo de la Atención</h3>
                  <p style="color:black;">
                    Contribuyen en el mejoramiento de la observación del ambiente y en la capacidad de percepción.
                    Permite orientar a la persona hacia el material, percibir y seleccionar lo más importante en las
                    situaciones o experiencias. A través de ellas se pretende fortalecer la búsqueda y el esfuerzo por
                    encontrar datos en cualquier ambiente, textos escritos o verbales, ya sea rico o pobre en estímulos
                    (Atención de búsqueda y vigilancia), captar estímulos de dos o más fuentes de datos (Atención dividida)
                    y enfocar esfuerzos en una sola dirección de los estímulos (Atención sostenida).
