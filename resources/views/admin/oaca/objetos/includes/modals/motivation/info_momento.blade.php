                  <h3 style="color:black;">Desarrollo de Imágenes Mentales</h3>
                  <br><br>
                  <p style="color:black;">


                    Si se desea que los estudiantes comprendan situaciones específicas, entonces hay que
                    conducirlos por las sendas de las Imágenes Mentales. Estas son muy útiles para abordar
                    informaciones ya sean espaciales o visuales ya que intervienen en la construcción del
                    pensamiento abstracto. Una forma de crear imágenes mentales es hacer asociaciones y
                    conexiones de la información con relatos, cuentos, anécdotas que favorezcan la imaginación.

                    Las imágenes mentales son rutas para el acceso al conocimiento, pero de una manera integrada
                    y holística. Una imagen mental, debidamente conducida, puede contribuir mucho más que una
                    clase de índole verbal y de transmisión. Las imágenes mentales facilitan la elaboración de
                    las actividades de comprensión de explicación, abstracción, relación, generalización, comparación,
                    entre otras, y también ocurre de forma inversa: estas actividades conducen a la creación de las
                    imágenes mentales. O sea, viene a ser un ciclo interminable de aprendizaje que coloca al
                    estudiante en una situación de ventaja en cuanto a la comprensión se refiere. Existen
                    innumerables formas de generar Imágenes Mentales, sin la necesidad de llevar a cabo grandes
                    esfuerzos, siempre y cuando se tenga presente qué actividades de comprensión se desea desarrollar.

                  </p>
